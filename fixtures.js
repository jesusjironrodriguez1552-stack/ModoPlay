// Funciones auxiliares para manejar el fixture
const fixtureHelper = {
    // Registrar resultado de un partido
    registrarResultado(fecha, equipoLocal, golesLocal, golesVisitante) {
        const fechaFixture = customFixtures.find(f => f.fecha === fecha);
        if (!fechaFixture) return false;
        
        const partido = fechaFixture.partidos.find(p => p.local === equipoLocal);
        if (!partido) return false;
        
        partido.resultado.golesLocal = golesLocal;
        partido.resultado.golesVisitante = golesVisitante;
        partido.resultado.jugado = true;
        return true;
    },

    // Agregar goleador
    agregarGoleador(fecha, equipoLocal, jugador, equipo, minuto) {
        const fechaFixture = customFixtures.find(f => f.fecha === fecha);
        if (!fechaFixture) return false;
        
        const partido = fechaFixture.partidos.find(p => p.local === equipoLocal);
        if (!partido) return false;
        
        partido.estadisticas.goleadores.push({
            jugador,
            equipo, // "local" o "visitante"
            minuto
        });
        return true;
    },

    // Agregar tarjeta amarilla
    agregarTarjetaAmarilla(fecha, equipoLocal, jugador, equipo, minuto) {
        const fechaFixture = customFixtures.find(f => f.fecha === fecha);
        if (!fechaFixture) return false;
        
        const partido = fechaFixture.partidos.find(p => p.local === equipoLocal);
        if (!partido) return false;
        
        partido.estadisticas.tarjetasAmarillas.push({
            jugador,
            equipo,
            minuto
        });
        return true;
    },

    // Agregar tarjeta roja
    agregarTarjetaRoja(fecha, equipoLocal, jugador, equipo, minuto, fechasExpulsion = 1) {
        const fechaFixture = customFixtures.find(f => f.fecha === fecha);
        if (!fechaFixture) return false;
        
        const partido = fechaFixture.partidos.find(p => p.local === equipoLocal);
        if (!partido) return false;
        
        partido.estadisticas.tarjetasRojas.push({
            jugador,
            equipo,
            minuto
        });
        
        // Agregar a la lista de expulsados
        partido.estadisticas.expulsados.push({
            jugador,
            equipo,
            fechasExpulsion
        });
        return true;
    },

    // Obtener jugadores expulsados para una fecha espec칤fica
    obtenerExpulsados(fecha) {
        const expulsados = [];
        for (let i = 1; i < fecha; i++) {
            const fechaFixture = customFixtures.find(f => f.fecha === i);
            if (fechaFixture) {
                fechaFixture.partidos.forEach(partido => {
                    partido.estadisticas.expulsados.forEach(exp => {
                        if (exp.fechasExpulsion > 0) {
                            expulsados.push({
                                jugador: exp.jugador,
                                equipo: exp.equipo,
                                fechaExpulsion: i,
                                fechasRestantes: exp.fechasExpulsion
                            });
                        }
                    });
                });
            }
        }
        return expulsados;
    },

    // Obtener estad칤sticas de un partido espec칤fico
    obtenerEstadisticasPartido(fecha, equipoLocal) {
        const fechaFixture = customFixtures.find(f => f.fecha === fecha);
        if (!fechaFixture) return null;
        
        const partido = fechaFixture.partidos.find(p => p.local === equipoLocal);
        return partido ? partido.estadisticas : null;
    }
};

